    private void jTextField1KeyReleased(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_jTextField1KeyReleased
        // TODO add your handling code here:
        ListaIds.removeAll(ListaIds);
        while(modelo.getRowCount()>0)modelo.removeRow(0);
        busqueda = jTextField1.getText();
        
        AutorDao daoA = new AutorDao();
        List<Autor> autores = daoA.consultaAvanzada(busqueda);
        modelo = (DefaultTableModel) jTable1.getModel();
        
        for (int i = 0; i < autores.size(); i++) {
            Autor temporal = autores.get(i);
            String[] fila = new String [4];
            fila[0] = temporal.getNombre();
            fila[1] = temporal.getApaterno();
            fila[2] = temporal.getAMaterno();
            if(temporal.isEstado()){
                fila[3] = "Activo";
            }else{
                fila[3] = "Inactivo";
            }
            modelo.addRow(fila);
            ListaIds.add(temporal.getIdAutor());
        }    
        if(jTable1.getRowCount() == 0){
            jLabel4.setVisible(true);
        }else{
            jLabel4.setVisible(false);
        }
    }//GEN-LAST:event_jTextField1KeyReleased

