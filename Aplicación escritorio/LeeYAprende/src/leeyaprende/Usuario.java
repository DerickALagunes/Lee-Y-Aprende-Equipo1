    private void jTextField1KeyReleased(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_jTextField1KeyReleased
        // TODO add your handling code here:
        while(modelo.getRowCount()>0)modelo.removeRow(0);
        String busqueda = jTextField1.getText();
        
        LibroDao daoL = new LibroDao();
        List<Libro> libros = daoL.consultaAvanzada(busqueda);
        modelo = (DefaultTableModel) jTable1.getModel();
        
        for (int i = 0; i < libros.size(); i++) {
            Libro temporal = libros.get(i);
            String[] fila = new String [6];
            fila[0] = temporal.getIsbn();
            fila[1] = temporal.getTitulo();
            fila[2] = temporal.getNum_paginas()+"";
            fila[3] = temporal.getAutor().getNombre()+" "+temporal.getAutor().getApaterno()+" "+temporal.getAutor().getAMaterno();
            fila[4] = temporal.getEditorial().getNombre();
            fila[5] = temporal.getArea().getNombreArea();
            modelo.addRow(fila);
        }
        if(jTable1.getRowCount() == 0){
            jLabel5.setVisible(true);
        }else{
            jLabel5.setVisible(false);
        }
    }//GEN-LAST:event_jTextField1KeyReleased

    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        // TODO add your handling code here:
        ConsultasDao conD = new ConsultasDao();
        List<Ejemplar> ejemplares = conD.consultarExistencias();
        
        String editoriales = "";
        for (int i = 0; i < ejemplares.size(); i++) {
            Ejemplar temporal = ejemplares.get(i);
            if (temporal.isEstado()) {
                editoriales += "-"+temporal.getLibro().getTitulo()+"\n";
            }else{
            editoriales += "-"+temporal.getLibro().getTitulo()+" (libro inactivo)\n";
            }
        }
        JOptionPane.showMessageDialog(this, "Libros con mayor existencia: \n"+editoriales);
    }//GEN-LAST:event_jButton2ActionPerformed

