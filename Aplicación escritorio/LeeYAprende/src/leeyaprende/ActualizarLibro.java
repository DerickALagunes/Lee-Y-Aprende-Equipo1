    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        if(!jTextField1.getText().equals("") && !jTextField2.getText().equals("") && !jTextField3.getText().equals("")
                && !jComboBox2.getSelectedItem().equals("Editorial") 
                && !jComboBox3.getSelectedItem().equals("Area")){
            Libro libro = new Libro();
            libro.setIsbn(jTextField1.getText());
            libro.setTitulo(jTextField2.getText());
            libro.setNum_paginas(Integer.parseInt(jTextField3.getText()));            
            
            String[] autorn = new String[modelo.size()];
            for (int i = 0; i < modelo.size(); i++) {
                autorn[i] = modelo.getElementAt(i).toString();
            }
            AutorDao daoA = new AutorDao();
            int[] idAutor = daoA.consultarPorNombreAxel(autorn);

            
            String editorialn = jComboBox2.getSelectedItem().toString();
            EditorialDao daoE = new EditorialDao();
            Editorial editorial = daoE.consultarPorNombre(editorialn);
                        
            String arean = jComboBox3.getSelectedItem().toString();
            AreaDao daoAr = new AreaDao();
            Area area = daoAr.consultarPorNombre(arean);
            
            libro.setEditorial(editorial);
            libro.setArea(area);
            
            LibroDao daoL = new LibroDao();
            if(daoL.modificar(idLibro, libro)){
                FichaDao daoF = new FichaDao();
                if(daoF.modificar(idLibro, idAutor)){
                    JOptionPane.showMessageDialog(this, "Libro actualizado");
                    AltaLibro altL = new AltaLibro();
                    altL.setVisible(true);
                    this.dispose();
                }                
            }else{
                JOptionPane.showMessageDialog(this, "Error al actualizar");
            }
        }else{
            JOptionPane.showMessageDialog(this, "Inserta todos los campos");
                    
        }
    }//GEN-LAST:event_jButton1ActionPerformed

    private void jTextField3KeyTyped(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_jTextField3KeyTyped
        // TODO add your handling code here:
        char c = evt.getKeyChar();
        
        if(!(Character.isDigit(c) || c==32 || c==8 || c==1)){
            evt.consume();
            JOptionPane.showMessageDialog(this, "Solo puedes escribir números");      
        }
        if(jTextField1.getText().length() == 45){
            evt.consume();
            JOptionPane.showMessageDialog(this, "Solo puedes escribir 45 caracteres");
        }
    }//GEN-LAST:event_jTextField3KeyTyped

