    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        String autor = jComboBox1.getSelectedItem().toString();
        if (autor.equals("Autor")) {
            JOptionPane.showMessageDialog(this, "Selecciona un autor");
        } else {
            if (modelo.contains(autor)) {
                JOptionPane.showMessageDialog(this, "Ya esta seleccionado");
            } else {
                modelo.addElement(autor);
                jList1.setModel(modelo);
            }
        }
    }//GEN-LAST:event_jButton3ActionPerformed

