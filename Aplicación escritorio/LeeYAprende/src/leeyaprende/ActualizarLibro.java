    DefaultListModel modelo = new DefaultListModel();
    
    public ActualizarLibro(int idLibro){
        initComponents();   
        
        AutorDao daoA = new AutorDao();
        List<Autor> autores = daoA.consultarNombre();
        for (int i = 0; i < autores.size(); i++) {
            jComboBox1.addItem(autores.get(i).getNombre()+" "+
                    autores.get(i).getApaterno()+" "+autores.get(i).getAMaterno());
        }
        
        
        EditorialDao daoE = new EditorialDao();
        List<String> editoriales = daoE.consultarNombre();
        for (int i = 0; i < editoriales.size(); i++) {
            jComboBox2.addItem(editoriales.get(i));
        }
        
        AreaDao daoAr = new AreaDao();
        List<String> areas = daoAr.consultarNombre();
        for (int i = 0; i < areas.size(); i++) {
            jComboBox3.addItem(areas.get(i));
        }
        
        setLocationRelativeTo(this);
                
        LibroDao daoL = new LibroDao();
        Libro libro = daoL.consultarPorId(idLibro);
        jTextField1.setText(libro.getIsbn());
        jTextField2.setText(libro.getTitulo());
        jTextField3.setText(libro.getNum_paginas()+"");
        
        ////////////////////trabajo
        
        FichaDao ficha = new FichaDao();
        List<Autor> lista = ficha.consultarAutoresDeLibro(idLibro);
        
        for (int i = 0; i < lista.size(); i++) {
            Autor temp = lista.get(i);
            String nom = temp.getNombre()+" "+temp.getApaterno()+" "+temp.getAMaterno();
            modelo.addElement(nom);
        }
        
        jList1.setModel(modelo);
        
       
        int idEditorial = libro.getEditorial().getIdEditorial();
        Editorial editorial = daoE.consultarPorId(idEditorial);
        jComboBox2.setSelectedItem(editorial.getNombre());
        
        int idArea = libro.getArea().getIdAreas();
        Area area = daoAr.consultarPorId(idArea);
        jComboBox3.setSelectedItem(area.getNombreArea());
        
        this.idLibro = idLibro;
    }
    
    

